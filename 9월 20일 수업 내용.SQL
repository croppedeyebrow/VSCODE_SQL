-- 외래키(FOREIGN KEY)는 **서로 다른 테이블간 관계를 정의하는 데 사용하는 제약 조건** 입니다.

-- 참조하고 있는 기본키의 데이터타입과 일치해야 하며,

--  **외래키에 참조되고 있는 기본키는 삭제 할 수 없습니다.**

CREATE TABLE DEPT_FK (
     DEPTNO NUMBER(2) PRIMARY KEY,
     DNAME  VARCHAR2(14),
     LOC    VARCHAR2(13)
);

CREATE TABLE EMP_FK(
     EMPNO NUMBER(4) PRIMARY KEY,
     ENAME  VARCHAR2(10) NOT NULL,
     JOB    VARCHAR2(9), 
     MGR    NUMBER(4), 
     SAL    NUMBER(7,2),
     COMM   NUMBER(7,2),
     DEPTNO NUMBER(2) REFERENCES DEPT_FK (DEPTNO)
);

