-- 테이블 변경하는 --ALTER 속에
    -- ADD : 테이블에 열을 추가
    -- RENAME : 열의 이름을 변경
    -- MODIFY: 열의 데이터형을 변경
    -- DROP: 열을 제거.

-- 테이블에 열을 추가 -- : ADD
ALTER TABLE EMP_DDL 
     ADD HP VARCHAR2(20);    

-- 테이블의 열 이름 변경 : RENAME
ALTER TABLE EMP_DDL
       RENAME COLUMN HP TO TEL;     

-- 테이블 열의 자료형 변경 : MODIFY
ALTER TABLE EMP_DDL
    MODIFY TEL VARCHAR2(20);


-- 자료형 크기를 변경할 때는, 기존 데이터의 크기를 고려해야 함.
-- 함수로 줄이는 작업을 하면 안됨.    

ALTER TABLE EMP_DDL  
    MODIFY DEPTNO NUMBER(4);

ALTER TABLE EMP_DDL    --열의 데이터가 있어도 삭제함.
       DROP COLUMN TEL;    


 -- 테이블 이름을 변경하는 RENAME
 RENAME EMP_DDL TO EMP_RENAME;
 SELECT * FROM EMP_RENAME;


 --테이블의 데이터를 삭제하는 TRUNCATE
 TRUNCATE TABLE EMP_RENAME;   -- DDL 명령어 [롤백 불가]

