-- 데이터 형태와 범위를 정하는 CHECK
-- EX) ID 및 PASSWORD 길이 제한.
-- 유료 값 범위 확인 (나이/성적/전화번호/이메일)

CREATE TABLE TABLE_CHECK (
    LOGIN_ID VARCHAR2(20) PRIMARY KEY,
    LOGIN_PW VARCHAR2(20) CHECK(LENGTH(LOGIN_PW) >= 5),
    TEL VARCHAR2(20)
);


